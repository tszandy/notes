# windows
ctrl + shift + o # bookmark manager
ctrl + h # history
ctrl + pageup   # jump to previous tab
ctrl + pagedown # jump to next tab
ctrl + shift + pageup # Move tabs left
ctrl + shift + pagedown # Move tabs right
ctrl + u # view page source

