# Steps to Export and Preserve Live Photos:
1. Select the Live Photos you want to export.
2. Go to File > Export > Export Unmodified Original for [X] Photos....
3. Choose a destination folder.
4. Exported files will include:
4.1. A still image (e.g., .HEIC or .JPEG).
4.2. A video file (e.g., .MOV).

# Re-import for Live Photo Recovery:
If you need the exported files to function as a Live Photo again:
1. Import both the still image and the .MOV file back into the Photos app.
2. Photos will automatically pair them to recreate the Live Photo.