# Run Cell
shift + enter

# Run All Cell
shift + alt + enter

# Insert Cell Above
Ctrl + Shift + A

# Insert Cell Below
Ctrl + Shift + B

# Delete Cell
D + D (press D twice)

# Move Cell Up
Ctrl + Shift + Up Arrow

# Move Cell Down
Ctrl + Shift + Down Arrow

# Convert to Markdown Cell
M (with the cell selected)

# Convert to Code Cell
Y (with the cell selected)

# Interrupt Kernel
I + I (press I twice)

# Restart Kernel
Ctrl + .
